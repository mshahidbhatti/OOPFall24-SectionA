public class PrimitiveTypes{

	public static void main(String ar[]){
		int i=0;
		char c='a';
		boolean b=true;
		System.out.printf("The data in Int: %d\n",i);
		System.out.printf("The data in char: %c\n",c);
		System.out.printf("The data in boolean: %b",b);

		System.out.println("\n The data in Int: "+i);


	}



}